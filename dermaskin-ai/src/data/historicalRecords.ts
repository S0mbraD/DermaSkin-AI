export function getRecordsByStudent(studentId: number): HistoricalRecord[] {
  return HISTORICAL_RECORDS.filter((r) => r.studentId === studentId);
}

export function getRecordsByCourse(courseId: string): HistoricalRecord[] {
  return HISTORICAL_RECORDS.filter((r) => r.courseId === courseId);
}

export function getStudentAvgScore(studentId: number): number {
  const records = getRecordsByStudent(studentId);
  if (records.length === 0) return 0;
  const total = records.reduce((sum, r) => sum + r.totalScore, 0);
  return Math.round((total / records.length) * 10) / 10;
}
